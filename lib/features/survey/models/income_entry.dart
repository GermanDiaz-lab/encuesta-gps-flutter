class IncomeEntry {
  IncomeEntry({
    required this.nombre,
    required this.empleo,
    required this.empleoNumero,
    required this.ocupacion,
    required this.ocupacionNumero,
    required this.telefono,
    required this.ingresos,
    required this.domicilio,
    required this.lugar,
  });

  final String nombre;
  final String empleo;
  final int empleoNumero;
  final String ocupacion;
  final int ocupacionNumero;
  final String telefono;
  final String ingresos;
  final String domicilio;
  final String lugar;
}
